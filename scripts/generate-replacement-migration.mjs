import { writeFile } from 'node:fs/promises';
import { resolve } from 'node:path';
import { createServer } from 'vite';

const outputPath = resolve(process.argv[2] ?? 'supabase/migrations/20260717000000_sticker_replacements.sql');

function sqlText(value) {
  return `'${String(value).replaceAll("'", "''")}'`;
}

const vite = await createServer({ appType: 'custom', server: { hmr: false, middlewareMode: true, ws: false } });
const { replacementStickers, stickers } = await vite.ssrLoadModule('/src/data/album.ts');
await vite.close();

if (stickers.length !== 980 || replacementStickers.length !== 118) {
  throw new Error('O catálogo deve conter 980 espaços originais e 118 substitutas.');
}

const originalById = new Map(stickers.map((sticker) => [sticker.id, sticker]));
const replacementValues = replacementStickers.map((replacement) => {
  const original = originalById.get(replacement.slotId);
  if (!original) throw new Error(`Espaço original ausente para ${replacement.label}.`);
  return `    (${sqlText(original.label)}, ${sqlText(replacement.number)}, ${sqlText(replacement.label)})`;
}).join(',\n');

const sql = `-- Generated by scripts/generate-replacement-migration.mjs.
-- Adds physical sticker variants without changing the album's 980 logical slots.

alter table public.stickers
  add column if not exists slot_id uuid,
  add column if not exists variant_type text not null default 'original';

update public.stickers
set slot_id = id
where slot_id is null;

alter table public.stickers
  alter column slot_id set not null,
  drop constraint if exists stickers_section_id_position_key,
  drop constraint if exists stickers_slot_id_fkey,
  drop constraint if exists stickers_variant_type_check,
  drop constraint if exists stickers_slot_consistency_check,
  add constraint stickers_slot_id_fkey foreign key (slot_id) references public.stickers(id) on delete cascade,
  add constraint stickers_variant_type_check check (variant_type in ('original', 'replacement')),
  add constraint stickers_slot_consistency_check check (
    (variant_type = 'original' and slot_id = id)
    or (variant_type = 'replacement' and slot_id <> id)
  );

create unique index if not exists stickers_original_section_position_unique
  on public.stickers (section_id, position)
  where variant_type = 'original';

create index if not exists stickers_slot_id_idx on public.stickers(slot_id);

with target_album as (
  select id from public.albums where slug = 'copa-2026'
), source_replacements (outgoing_label, code, incoming_label) as (
  values
${replacementValues}
), original_slots as (
  select stickers.*
  from public.stickers
  join target_album on target_album.id = stickers.album_id
  where stickers.variant_type = 'original'
)
insert into public.stickers (
  album_id, section_id, code, label, position, slot_id, variant_type
)
select
  original_slots.album_id,
  original_slots.section_id,
  source_replacements.code,
  source_replacements.incoming_label,
  original_slots.position,
  original_slots.id,
  'replacement'
from source_replacements
join original_slots on original_slots.label = source_replacements.outgoing_label
on conflict (album_id, code) do update set
  section_id = excluded.section_id,
  label = excluded.label,
  position = excluded.position,
  slot_id = excluded.slot_id,
  variant_type = excluded.variant_type;

do $$
declare
  replacement_count integer;
begin
  select count(*) into replacement_count
  from public.stickers
  join public.albums on albums.id = stickers.album_id
  where albums.slug = 'copa-2026'
    and stickers.variant_type = 'replacement';

  if replacement_count <> 118 then
    raise exception 'Expected 118 replacement stickers, found %', replacement_count;
  end if;
end;
$$;

-- Public lists continue to expose only the selected missing/duplicate view.
-- Missing is calculated per logical slot; duplicates are calculated per
-- physical variant, so owning one original and one replacement is not a repeat.
create or replace function public.get_collection_share(share_token uuid)
returns jsonb
language sql
stable
security definer
set search_path = ''
as $$
  select jsonb_build_object(
    'ownerName', coalesce(nullif(profiles.display_name, ''), nullif(profiles.username, ''), 'Colecionador ColaFig'),
    'albumSlug', albums.slug,
    'albumName', albums.name,
    'viewType', collection_shares.view_type,
    'items', coalesce(
      jsonb_agg(
        jsonb_build_object(
          'code', shared_items.code,
          'label', shared_items.label,
          'sectionName', shared_items.section_name,
          'quantity', shared_items.quantity
        ) order by shared_items.section_position, shared_items.sticker_position, shared_items.variant_type
      ) filter (where shared_items.code is not null),
      '[]'::jsonb
    )
  )
  from public.collection_shares
  join public.albums on albums.id = collection_shares.album_id and albums.is_active = true
  left join public.profiles on profiles.id = collection_shares.user_id
  left join lateral (
    select
      stickers.code,
      stickers.label,
      sections.name as section_name,
      sections.position as section_position,
      stickers.position as sticker_position,
      stickers.variant_type,
      coalesce(collections.quantity, 0)::smallint as quantity
    from public.stickers
    join public.sections on sections.id = stickers.section_id
    left join public.collections
      on collections.user_id = collection_shares.user_id
      and collections.sticker_id = stickers.id
    where stickers.album_id = albums.id
      and (
        (
          collection_shares.view_type = 'missing'
          and stickers.variant_type = 'original'
          and not exists (
            select 1
            from public.stickers as slot_variant
            join public.collections as slot_collection
              on slot_collection.sticker_id = slot_variant.id
              and slot_collection.user_id = collection_shares.user_id
              and slot_collection.quantity > 0
            where slot_variant.slot_id = stickers.id
          )
        )
        or (
          collection_shares.view_type = 'duplicates'
          and coalesce(collections.quantity, 0) > 1
        )
      )
  ) as shared_items on true
  where collection_shares.token = share_token
    and collection_shares.revoked_at is null
  group by collection_shares.id, profiles.display_name, profiles.username, albums.id;
$$;

revoke all on function public.get_collection_share(uuid) from public;
grant execute on function public.get_collection_share(uuid) to anon, authenticated;
`;

await writeFile(outputPath, sql, 'utf8');
console.log(`Migration gerada em ${outputPath}`);
