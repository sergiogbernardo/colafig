import { writeFile } from 'node:fs/promises';
import { resolve } from 'node:path';
import { createServer } from 'vite';

const outputPath = resolve(process.argv[2] ?? 'supabase/migrations/20260714003000_seed_copa_2026.sql');

function sqlText(value) {
  return `'${String(value).replaceAll("'", "''")}'`;
}

const vite = await createServer({ appType: 'custom', server: { hmr: false, middlewareMode: true, ws: false } });
const { albumCatalog, sections, stickers } = await vite.ssrLoadModule('/src/data/album.ts');
await vite.close();

const album = albumCatalog.find((item) => item.slug === 'copa-2026');
if (!album || sections.length !== 49 || stickers.length !== 980) {
  throw new Error('O catálogo Copa 2026 está incompleto; a migration não foi gerada.');
}

const sectionPositions = new Map(sections.map((section) => [section.id, 0]));
const sectionValues = sections.map((section, position) =>
  `    (${sqlText(section.id)}, ${sqlText(section.name)}, ${position})`,
).join(',\n');
const stickerValues = stickers.map((sticker) => {
  const position = sectionPositions.get(sticker.section);
  sectionPositions.set(sticker.section, position + 1);
  return `    (${sqlText(sticker.section)}, ${sqlText(sticker.number)}, ${sqlText(sticker.label)}, ${position})`;
}).join(',\n');

const sql = `-- Generated by scripts/generate-catalog-migration.mjs.
-- Idempotently publishes the first ColaFig catalogue entry.

insert into public.albums (
  slug, name, year, is_active, category, publisher, edition, description, accent_color
)
values (
  ${sqlText(album.slug)}, ${sqlText(album.name)}, ${album.year}, false,
  ${sqlText(album.category)}, null, ${sqlText(String(album.year))},
  ${sqlText(album.description)}, ${sqlText(album.accent)}
)
on conflict (slug) do update set
  name = excluded.name,
  year = excluded.year,
  category = excluded.category,
  publisher = excluded.publisher,
  edition = excluded.edition,
  description = excluded.description,
  accent_color = excluded.accent_color,
  is_active = false;

with target_album as (
  select id from public.albums where slug = ${sqlText(album.slug)}
), source_sections (slug, name, position) as (
  values
${sectionValues}
)
insert into public.sections (album_id, slug, name, position)
select target_album.id, source_sections.slug, source_sections.name, source_sections.position
from target_album cross join source_sections
on conflict (album_id, slug) do update set
  name = excluded.name,
  position = excluded.position;

with target_album as (
  select id from public.albums where slug = ${sqlText(album.slug)}
), source_stickers (section_slug, code, label, position) as (
  values
${stickerValues}
)
insert into public.stickers (album_id, section_id, code, label, position)
select target_album.id, sections.id, source_stickers.code, source_stickers.label, source_stickers.position
from target_album
join public.sections on sections.album_id = target_album.id
join source_stickers on source_stickers.section_slug = sections.slug
on conflict (album_id, code) do update set
  section_id = excluded.section_id,
  label = excluded.label,
  position = excluded.position;

update public.albums
set is_active = true
where slug = ${sqlText(album.slug)};
`;

await writeFile(outputPath, sql, 'utf8');
console.log(`Migration gerada em ${outputPath}`);
